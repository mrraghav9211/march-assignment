function toggle(){
    var toggle = document.getElementsByClassName('popup')[0];
    if(toggle.style.display === "block"){
        toggle.style.display = "none"
    } else{
        toggle.style.display = "block"
    }
}
function hide(){
    var toggle = document.getElementsByClassName('popup')[0];
   if(toggle.style.display ==="block"){
       toggle.style.display = "none"
   }
}